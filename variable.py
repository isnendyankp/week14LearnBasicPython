# membuat variabel dengan nama kucing dan beri nilai "
kucing = "cat"

print(kucing)
# type() untuk mengetahui tipe data dari variabel dan hasilnya adalah str
print(type(kucing))

# membuat variabel dengan nama kucing dan beri nilai 4
kucing = 4

print(kucing)
# type() untuk mengetahui tipe data dari variabel dan hasilnya adalah int
print(type(kucing))

# membuat variabel dengan beberapa tipe data

# string
animal = "wolf"
# integer
animal_total = 3
# float
currency = 1.5
# boolean
boolean = True

print(f"animal: {type(animal)}")
print(f"animal_total: {type(animal_total)}")
print(f"currency: {type(currency)}")
print(f"boolean: {type(boolean)}")